
/// <summary>
/// In this imaginary game world, three kinds of "affinity" exist:
/// Sun, Moon and Stars. The idea is certain places (e.g. dungeons)
/// have an "affinity". If e.g. a Dungeon has the Moon affinity,
/// you can ONLY use Gear with Moon affinity in that particular Dungeon.
/// </summary>
public enum AffinityType
{
    Sun,
    Moon,
    Stars
}

